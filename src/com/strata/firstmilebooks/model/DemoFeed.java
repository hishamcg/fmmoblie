package com.strata.firstmilebooks.model;

public class DemoFeed {

	private String publisher_name;
	private String publisher_image;
	private String product_list_name;
	private String product_list_type;
	private String date;
	private String time;
	private String description;
	private String cover_image;
	
	public String getPublisher_name() {
		return publisher_name;
	}
	public void setPublisher_name(String publisher_name) {
		this.publisher_name = publisher_name;
	}
	public String getPublisher_image() {
		return publisher_image;
	}
	public void setPublisher_image(String publisher_image) {
		this.publisher_image = publisher_image;
	}
	public String getProduct_list_name() {
		return product_list_name;
	}
	public void setProduct_list_name(String product_list_name) {
		this.product_list_name = product_list_name;
	}
	public String getProduct_list_type() {
		return product_list_type;
	}
	public void setProduct_list_type(String product_list_type) {
		this.product_list_type = product_list_type;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getCover_image() {
		return cover_image;
	}
	public void setCover_image(String cover_image) {
		this.cover_image = cover_image;
	}
	
	
}
